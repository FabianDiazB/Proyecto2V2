    public void crearCombo(){
        int i=1;
        for(Partida p: user.getPartidas()){
            jComboBox1.addItem(p.getNombre(i));
            i++;
        }
        
        if(jComboBox1.getItemCount() == 0){
                btnCargar.setEnabled(false);
            }
    }

